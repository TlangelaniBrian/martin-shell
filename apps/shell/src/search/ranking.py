from dataclasses import dataclass, field
from typing import Optional
from rapidfuzz import fuzz


# ── Data types (mirror martin/common types/search.ts) ─────────────────────

@dataclass
class CaseData:
    id: int
    case_name: str
    court: str
    date_decided: Optional[str] = None
    citation: Optional[str] = None
    summary: Optional[str] = None
    pdf_url: Optional[str] = None
    saflii_url: str = ""
    judges: list[str] = field(default_factory=list)


@dataclass
class SearchParams:
    q: Optional[str] = None
    court: Optional[str] = None
    year_from: Optional[str] = None
    year_to: Optional[str] = None
    judge: Optional[str] = None
    party: Optional[str] = None
    page: Optional[str] = None
    source: Optional[str] = None


# ── Scoring helpers ────────────────────────────────────────────────────────

def _similarity(a: str, b: str) -> float:
    return fuzz.partial_ratio(a.lower(), b.lower()) / 100.0


def _positional_similarity(query_terms: list[str], text: str) -> float:
    """Score based on how early and close query terms appear in text."""
    text = text.lower()
    positions = [
        min(text.find(term), len(text)) if term in text else len(text)
        for term in query_terms
    ]
    return 1.0 / (1.0 + sum(positions) / len(query_terms))


# SA court hierarchy — ConCourt authority ranks highest
_COURT_BOOST = {
    "Constitutional Court": 2.0,
    "Supreme Court of Appeal": 1.5,
    "High Court": 1.0,
}


def _score_case(query: SearchParams, case: CaseData) -> float:
    score = 0.0
    terms = (query.q or "").lower().strip().split()
    if not terms:
        return 0.0

    W_CASE_NAME = 1.5
    W_CITATION  = 1.0
    W_SUMMARY   = 1.0
    W_COURT     = 0.5

    if case.case_name:
        cn_score = _positional_similarity(terms, case.case_name)
        if any(t in case.case_name.lower() for t in terms):
            cn_score = max(W_CASE_NAME, cn_score)
        score += W_CASE_NAME * cn_score

    if case.citation:
        cit_score = sum(_similarity(t, case.citation) for t in terms) / len(terms)
        if all(t in case.citation.lower() for t in terms):
            cit_score = W_CITATION
        score += W_CITATION * cit_score

    if case.summary:
        sum_score = _positional_similarity(terms, case.summary[:200])
        if all(t in case.summary.lower() for t in terms):
            sum_score = W_SUMMARY
        score += W_SUMMARY * sum_score

    if case.court:
        if any(t in case.court.lower() for t in terms):
            score += W_COURT
        for court_name, boost in _COURT_BOOST.items():
            if court_name.lower() in case.court.lower():
                score *= boost
                break

    # Hard filters
    if query.court and case.court:
        if query.court.lower() not in case.court.lower():
            return 0.0
    if query.year_from and case.date_decided:
        if case.date_decided[:4] < query.year_from:
            return 0.0
    if query.year_to and case.date_decided:
        if case.date_decided[:4] > query.year_to:
            return 0.0
    if query.judge and case.judges:
        if not any(query.judge.lower() in j.lower() for j in case.judges):
            return 0.0

    return score


# ── Public API ─────────────────────────────────────────────────────────────

def rank(query: SearchParams, corpus: list[CaseData]) -> list[CaseData]:
    """
    Rank a list of cases by relevance to the query.
    Returns the corpus sorted descending by relevance score.
    """
    scored = [(case, _score_case(query, case)) for case in corpus]
    scored.sort(key=lambda x: x[1], reverse=True)
    return [case for case, _ in scored]
